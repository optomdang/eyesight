const express = require('express');
const auth = require('../../../middlewares/auth');
const validate = require('../../../middlewares/validate');
const roleValidation = require('../../../validations/authentication/role.validation');
const roleController = require('../../../controllers/authentication/role.controller');
const injectData = require('../../../middlewares/injectData');
const allRights = require('../../../config/rights');

const router = express.Router();

router
  .route('/')
  .post(auth(allRights.manageRoles.code), validate(roleValidation.createRole), injectData('body'), roleController.createRole)
  .get(auth(allRights.getRoles.code), validate(roleValidation.getRoles), injectData('query'), roleController.getRoles)
  .delete(
    auth(allRights.manageRoles.code),
    validate(roleValidation.deleteRoles),
    injectData('body'),
    roleController.deleteRoles
  );

router
  .route('/:roleId')
  .get(auth(allRights.getRoles.code), validate(roleValidation.getRole), roleController.getRole)
  .patch(
    auth(allRights.manageRoles.code),
    validate(roleValidation.updateRole),
    injectData('body'),
    roleController.updateRole
  )
  .delete(
    auth(allRights.manageRoles.code),
    validate(roleValidation.deleteRole),
    injectData('body'),
    roleController.deleteRole
  );

module.exports = router;

